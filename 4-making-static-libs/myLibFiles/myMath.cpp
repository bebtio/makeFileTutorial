#include "myMath.hpp"



/************************************************************/
// Takes two numbers, a and b, and returns their sum.
/************************************************************/
int add( int a, int b )
{

  int sum( a + b );

  return( sum );

}


//***********************************************************/
// Takes two numbers, a and b, and returns their difference.
//***********************************************************/
int diff( int a, int b )
{

  int diff( a - b );

  return( diff );

}
